  setPartnersDailyReports: (reports) => set({ partnersDailyReports: reports }),
  setRegions: (regions) => set((state) => ({ regions })),
  setCities: (cities) => set((state) => ({ cities })),
  setLicenses: (licenses) => set((state) => ({ licenses })),
