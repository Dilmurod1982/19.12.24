  setHokim: (hokim) => set((state) => ({ hokim })),
  setContract: (contract) => set((state) => ({ contract })),
  setApz: (apz) => set((state) => ({ apz })),
  setGastx: (gastx) => set((state) => ({ gastx })),
  setElektrtx: (elektrtx) => set((state) => ({ elektrtx })),
